$(function() {
  $('#adv-card1').hover(function() {
    $('#icon-adv-1').css('color', '#00ACC1');
    $('#adv-card1').addClass('shadow-lg');

  }, function() {
    // on mouseout, reset the background colour
    $('#icon-adv-1').css('color', '#26C6DA');
    $('#adv-card1').removeClass('shadow-lg');
  });
});

